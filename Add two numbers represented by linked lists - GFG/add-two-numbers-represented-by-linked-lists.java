class Solution{
    //Function to add two numbers represented by linked list.
    static Node addTwoLists(Node first, Node second){
        // code here
        // return head of sum list
        int carry = 0;
        Node dummy = new Node(0);
        first = reverse(first);
        second = reverse(second);
        Node p = dummy;
        while (first != null || second != null || carry != 0) {
            if (first != null) {
            carry += first.data;
            first = first.next;
            }
            if (second != null) {
            carry += second.data;
            second = second.next;
            }
            p.next = new Node(carry%10);
            carry /= 10;
            p = p.next;
        }
        return reverse(dummy.next);
    }
    public static Node reverse(Node head){
        Node curr = head;
        Node next = null;
        Node prev = null;
        while(curr!=null){
            next = curr.next;
            curr.next = prev;
            prev = curr;
            curr = next;
        }
        return prev;
    }
}